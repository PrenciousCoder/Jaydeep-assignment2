class Calculator:
    def __init__(self,num1,num2):
        self.num1=num1
        self.num2=num2
    def addition(self):
        temp=self.num1+self.num2
        print(temp)
    def subtraction(self):
        temp=self.num1-self.num2
        print(temp)
    def multiplication(self):
        temp=self.num1*self.num2
        print(temp)
    def Division(self):
        temp=self.num1/self.num2
        print(temp)
    def Quotient(self):
        temp=self.num1//self.num2
        print(temp)
    def Remainder(self):
        temp=self.num1%self.num2
        print(temp)

